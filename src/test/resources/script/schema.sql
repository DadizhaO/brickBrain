
CREATE SCHEMA IF NOT EXISTS MA_STUDENT;

CREATE SEQUENCE IF NOT EXISTS OFFICE_SEQ
  MINVALUE 5555555555
  MAXVALUE 9999999999
  START WITH 5555555556
             INCREMENT BY 1;

DROP TABLE IF EXISTS MA_STUDENT.OFFICES;

CREATE TABLE MA_STUDENT.OFFICES
(	OFFICE DECIMAL(38,0),
   MGR DECIMAL (38, 0),
   CITY VARCHAR (0),
   REGION VARCHAR (0),
   TARGET DECIMAL(38,0),
   SALES DECIMAL (38,0)
)

